package JavaBasicPrograms;

public class Datatypes {
	
	public static void main(String args[])
	{
		//int - 4 bytes (range 2147483647 to -2147483648)
		int a=10;
		System.out.println("Integer value a is "+a);
		
		//char - 2 bytes (stores one character)
		char c='E';
		System.out.println("Character value c is "+c);
		
		//short - 2 bytes (range 32767 to -32768)
		short s=31456;
		System.out.println("Short value s is "+s);
		
		//long - 8 bytes (range 9223372036854775807L to -9223372036854775808L) must mention 'L' while using long
		long l=9345671234L;
		System.out.println("Long value l is "+l);
		
		//float - 4 bytes (stores decimal values) must mention 'f' while using float
		float f=10.432f;
		System.out.println("Float value f is "+f);
		
		//double - 8 bytes (stores greater decimal values) here also mention 'f' while using double
		double d=4301.5485025793025802;
		System.out.println("Double value d is "+d);
		
		//byte - 1 byte (range 127 to -128)
		byte b=120;
		System.out.println("Byte value b is "+b);
		
		//boolean (Stores true or false)
		boolean k=false;
		System.out.println("Boolean value k is "+k);
		
		//String (stores set of characters)
		String n="/Deepak16$";
		System.out.println("String value n is "+n);
		
	}

}
